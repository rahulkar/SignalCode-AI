import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const catalogPath = path.join(root, "model-catalog.json");
const litellmConfigPath = path.join(root, "litellm", "config.yaml");
const backendGeneratedPath = path.join(root, "backend", "src", "modelCatalog.generated.ts");

const catalog = JSON.parse(fs.readFileSync(catalogPath, "utf8"));

if (!catalog.defaultModel || !Array.isArray(catalog.models) || catalog.models.length === 0) {
  throw new Error("Invalid model-catalog.json format.");
}

const aliases = catalog.models.map((m) => m.alias);
if (!aliases.includes(catalog.defaultModel)) {
  throw new Error("defaultModel must be one of models[].alias.");
}

const litellmYaml = [
  "model_list:",
  ...catalog.models.flatMap((m) => [
    `  - model_name: ${m.alias}`,
    "    litellm_params:",
    `      model: ${m.litellmModel}`,
    "      api_key: os.environ/GEMINI_API_KEY"
  ]),
  "",
  "litellm_settings:",
  "  set_verbose: true",
  ""
].join("\n");

const backendTs = `// AUTO-GENERATED by scripts/sync-model-catalog.mjs
// Do not edit manually; update model-catalog.json and rerun the script.
export const defaultModel = ${JSON.stringify(catalog.defaultModel)} as const;
export const supportedModels = ${JSON.stringify(aliases, null, 2)} as const;
`;

const checkMode = process.argv.includes("--check");
const files = [
  { path: litellmConfigPath, expected: litellmYaml },
  { path: backendGeneratedPath, expected: backendTs }
];

if (checkMode) {
  const drifted = files.filter((file) => {
    if (!fs.existsSync(file.path)) {
      return true;
    }
    const current = fs.readFileSync(file.path, "utf8");
    return current !== file.expected;
  });
  if (drifted.length > 0) {
    const relative = drifted.map((file) => path.relative(root, file.path)).join(", ");
    console.error(`Model catalog drift detected in: ${relative}`);
    console.error("Run: node scripts/sync-model-catalog.mjs");
    process.exit(1);
  }
  console.log("Model catalog is in sync.");
} else {
  fs.writeFileSync(litellmConfigPath, litellmYaml, "utf8");
  fs.writeFileSync(backendGeneratedPath, backendTs, "utf8");
  console.log("Synced model catalog to litellm/config.yaml and backend/src/modelCatalog.generated.ts");
}
